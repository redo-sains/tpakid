                        <div class="content">
                            <p style="text-align: justify">
                                <span style="font-size: 12pt">Sampit &ndash; Pangkalan Bun, 5 - 7
                                    Oktober 2021.&nbsp; Otoritas Jasa
                                    Keuangan (OJK) Provinsi Kalimantan
                                    Tengah bersama Pemerintah Kabupaten
                                    Kotawaringin Timur dan Pemerintah
                                    Kabupaten Kotawaringin Barat
                                    melaksanakan kegiatan Rapat
                                    Koordinasi Tim Percepatan Akses
                                    Keuangan Daerah (TPAKD).</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">Kegiatan Rapat Koordinasi TPAKD
                                    Kabupaten Kotawaringin Timur
                                    dilaksanakan pada hari Selasa 5
                                    Oktober 2021 bertempat di Aula
                                    Kantor Bupati Kotawaringin Timur
                                    yang dihadiri oleh Wakil Bupati
                                    Kotawaringin Timur, Kepala OJK
                                    Provinsi Kalimantan Tengah, dan
                                    Asisten Bidang Perekonomian dan
                                    Pembangunan Kabupaten Kotawaringin
                                    Timur serta Anggota TPAKD Kabupaten
                                    Kotawaringin Timur.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">Dalam sambutan Bupati Kotawaringin
                                    Timur yang di bacakan oleh Wakil
                                    Bupati Kotawaringin Timur, Irawati,
                                    S.Pd. &nbsp;saat membuka langsung
                                    pelaksanaan Rapat Koordinasi TPAKD,
                                    menyampaikan apresiasi dan menyambut
                                    baik atas pelaksanaan Rapat
                                    Koordinasi TPAKD Kabupaten
                                    Kotawaringin Timur dalam rangka
                                    mendukung program Pemerintah untuk
                                    memberikan pelayanan kepada
                                    masyarakat dalam mengakses layanan
                                    perbankan dan layanan pemerintah di
                                    bidang keungan khususnya layanan
                                    pinjaman modal melalui bank.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">&ldquo;Sesuai Arahan Presiden
                                    Republik Indonesia, Bapak Joko
                                    WiDodo, diminta kepada setiap Kepala
                                    Daerah untuk melakukan cara-cara
                                    yang i TPKADtif dan cepat dalam
                                    menjalankan segala kebijakan dan
                                    program yang ada, seperti
                                    peningkatan literasi kuangan melaui
                                    cara &nbsp;yang i TPKADtif, mendorong
                                    kelompok-kelompok usaha, penguatan
                                    infrastruktur keuangan daerah yang
                                    agresif dan peningkatan tingkat
                                    inklusi keuangan melalui produk dari
                                    lembaga keuangan yang diakses
                                    masyarkat luas&rdquo; kata
                                    Irawati.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">Sementara itu, Kepala OJK
                                    Kalimantan Tengah, Otto Fitriandy
                                    dalam sambutannya yang hadir melalui
                                    virtual zoom menyampaikan, sangat
                                    mengapresiasi kegiatan Rapat
                                    Koordinasi TPAKD di Kabupaten
                                    Kotawaringin Timur selama ini sudah
                                    cukup intens berkomonikasi dengan
                                    OJK melalui Biro Ekonomi dalam
                                    menyampaikan perkembangan Tim
                                    Percepatan Akses Keuangan Daerah di
                                    Kabupaten Kotawaringin Timur.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">&ldquo;Kami berharap dalam Rapat
                                    Koordinasi TPAKD di Kabupaten
                                    Kotawaingin Timur kali ini dapat
                                    mendiskusikan hal-hal yang menjadi
                                    kendala dalam pelaksanaan kegiatan
                                    Tim Percepatan Akses Keuangan Daerah
                                    Kabupaten Kotawaringin Timur agar
                                    menjadi pembahasan bersama untuk
                                    dicari solusinya&rdquo; ujar
                                    Otto.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">TPAKD Kabupaten Kotawaringin Timur
                                    dibentuk agar masyarakat di wilayah
                                    Kabupaten Kotawaringin Timur dapat
                                    mengakses layanan perbankan dan
                                    layanan pemerintah di bidang
                                    keuangan, khususnya layanan
                                    pemberian pinjaman modal melalui
                                    bank atau bantuan sarana prasarana
                                    usaha dari pemerintah, sehingga
                                    bantuan tersebut dapat dinikmati
                                    masyarakat itu sendiri.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">Kegiatan tersebut membahas
                                    penyampaian realisasi program dan
                                    hambatan pada beberapa bidang dalam
                                    pelaksanaan program TPAKD Kabupaten
                                    Kotawaringin Timur diantaranya
                                    bidang pertanian, bidang
                                    pemberdayaan masyarakat dan desa,
                                    bidang perikanan, bidang koperasi
                                    dan usaha kecil menengah, bidang
                                    perdagangan dan induistri, industri
                                    perbankan dan Pengurus LKM
                                    Hatantiring Manggatang Utus.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">Selanjutnya, Kepala Sub Bagian
                                    Edukasi dan Perlindungan Konsumen
                                    OJK menindaklanjuti beberapa hal
                                    yang disampaikan dalam pembahasan
                                    pada kegiatan tersebut, yaitu dengan
                                    melakukan konfirmasi kembali terkait
                                    dengan asuransi kepada nelayan,
                                    pelatihan laporan keuangan kepada
                                    BUMDes yang akan segera
                                    ditindaklanjuti, OJK bekerjasama
                                    dengan Kementrian Hukum dan HAM
                                    Provinsi Kalimantan Tengah mendorong
                                    untuk mendukung pembentukan
                                    perusahaan perseorangan kepada
                                    BUMDes sehingga tidak ada hambatan
                                    terhadap akses keuangan di
                                    perbankan, dan pengembangan
                                    agen-agen Laku Pandai pada industri
                                    perbankan di Kabupaten Kotawaringin
                                    Timur.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">Selanjutnya, kegiatan dilanjutkan
                                    dengan agenda
                                    <em>High Level Meeting</em> Tim
                                    Percepatan Akses&nbsp; Keuangan
                                    Daerah (TPAKD) Kabupaten
                                    Kotawaringin Barat yang dilaksanakan
                                    bertempat di Aula Kantor Bupati
                                    Kotawaringin Barat pada hari Kamis,
                                    7 Oktober 2021. Kegiatan dimaksud
                                    bertepatan dengan hari jadi
                                    Kabupaten Kotawaringin Barat
                                    ke-62.&nbsp; Hadiri dalam kegiatan
                                    tersebut Wakil Bupati Kotawaringin
                                    Barat, Kepala Otoritas Jasa Keuangan
                                    Provinsi Kalimantan Tengah dan
                                    Asisten Bidang Perekonomian dan
                                    Pembangunan Kotawaringin Barat serta
                                    anggota TPAKD Kabupaten Kotawaringin
                                    Barat.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">Bupati Kotawaringin Barat membuka
                                    dan memberikan sambutan dalam
                                    pelaksanaan kegiatan Rapat
                                    Koordinasi TPAKD, yang dibacakan
                                    oleh Wakil Bupati Kotawaringin
                                    Barat, Ahmadi Riansyah menyampaikan
                                    selamat datang di Bumi Marunting
                                    Batu Aji kepada Kepala OJK beserta
                                    rombongan dan mengapresiasi kegiatan
                                    <em>High Level Meeting</em> Tim
                                    Percepatan Akses Keuangan Daerah
                                    Kabupaten Kotawaringin Barat kali
                                    ini, sebagai salah satu implementasi
                                    <em>Roadmap</em> Tim Percepatan
                                    Akses&nbsp; Keuangan Daerah
                                    2021-2025 dalam rangka wadah
                                    koordinasi untuk mempercepat akses
                                    keuangan di Kabupaten Kotawaringin
                                    Barat.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">&ldquo;Berdasarkan data dan
                                    informasi yang saya dapat bahwa
                                    potensi-potensi aktivitas keuangan
                                    di daerah kita sangat laur biasa,
                                    dari jumlah penduduk yang hampir
                                    tiga ratus ribu jiwa yang di topang
                                    sumber daya alam terutama sektor
                                    perkebunan yang sangat luar biasa,
                                    saya sakin potensi ini menjadi modal
                                    dalam Percepatan akses keuangan
                                    daerah&rdquo; kata Ahmadi.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">Sementara itu, Kepala OJK
                                    Kalimantan Tengah, Otto Fitriandy
                                    dalam sambutannya mengucapkan
                                    selamat hari ulang tahun ke-62
                                    kepada Kabupaten Kotawaringin Barat,
                                    Bumi Marunting Batu Aji, serta
                                    mengapresiasi kepada Pemerintah
                                    Kabupaten Kotawaringin Barat atas
                                    dukungannya kepada pelaksanaan
                                    <em>High Level Meeting</em> Tim
                                    Percepatan Akses&nbsp; Keuangan
                                    Daerah secara keseluruhan.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">&ldquo;Harusnya Tim TPAKD ini bisa
                                    menjadi <em>supporting</em> atau
                                    melakukan pelaksanaan kegiatannya
                                    dengan menyesuaikan program
                                    pembangunan khususnya di daerah
                                    Kotawaringin Barat, sehingga hal-hal
                                    yang harusnya bisa menjadi
                                    kontribusi yang memperbaiki secara
                                    keseluruhan jika dilakukan
                                    bersama-sama akan jauh lebih baik
                                    dibandingkan teman-teman Industri
                                    Jasa Keuangan melakukan aktivitasnya
                                    masing-masing tetapi tidak sesuai
                                    dengan visi misi pembangunan
                                    Kabupaten Kotawaringin Barat&rdquo;
                                    ujar Otto.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">Kegiatan dilanjutkan dengan diskusi
                                    dan pembahasan terkait dengan
                                    penyampaian <em>Update </em>Laporan
                                    Realisasi Program TPAKD Kabupaten
                                    Kotawaringin Barat Tahun 2021 serta
                                    kendala-kendala yang dihadapi, dan
                                    mendengarkan usulan-usulan dari
                                    masing-masing anggota TPAKD
                                    Kabupaten Kotawaringin Barat.</span>
                            </p>

                            <p style="text-align: justify">
                                <span style="font-size: 12pt">Kegiatan
                                    <em>High Level Meeting</em> Tim
                                    Percepatan Akses Keuangan Daerah
                                    Kabupaten Kotawaringin Barat
                                    dilaksanakan untuk dapat
                                    meningkatkan sinergi antara instansi
                                    guna merumuskan dan menyusun
                                    program-program TPAKD, sehingga
                                    diharapkan mampu menciptakan
                                    semangat yang baru dalam mendorong
                                    percepatan pertumbuhan dan pemulihan
                                    perekonomian bagi masyarakat di
                                    Kalimantan Tengah khususnya di
                                    wilayah Kabupaten Kotawaringin
                                    Barat, selain itu pada kegiatan
                                    tersebut juga diluncurkan SiROKED
                                    KOBAR, yaitu &lsquo;Sistem Informasi
                                    <em>Roadmap</em> Aksebilitas
                                    Keuangan Daerah Kotawaraingin
                                    Barat&rsquo;, dengan tujuan
                                    memudahkan masyarakat untuk
                                    mengakses informasi berbagai produk
                                    lembaga keuangan maupun non
                                    keuangan, agar menjadi rujukan dalam
                                    mengambil keputusan terhadap
                                    Perencanaan Keuangan.</span>
                            </p>

