line-height:150%'><span lang=EN-ID style='font-size:12.0pt;line-height:150%;
font-family:"Times New Roman",serif'>Laporan kinerja adalah laporan berisi
                                hasil dari pelaksanaan suatu kegiatan atau pekerjaan perseorangan atau
                                perusahaan atas penggunaan anggaran. Laporan kinerja dibuat untuk memfasilitasi
                                manajemen dalam melakukan pengendalian intern, serta untuk menunjukkan bahwa
                                anggaran dibelanjakan secara efisien dan bermanfaat pada kegiatan operasional
                                perusahaan. Laporan kinerja yang disiapkan dan disampaikan untuk bahan
                                monitoring, tentunya periodisasi pelaporannya berjangka pendek misalnya bulanan
                                atau triwulanan. Akan tetapi, laporan kinerja untuk tujuan pertanggung jawaban
                                cukuplah disusun dan disampaikan setahun sekali. </span></p>

                <p class=MsoListParagraphCxSpLast style='margin-top:0in;margin-right:0in;
margin-bottom:0in;margin-left:21.3pt;text-align:justify;text-indent:28.35pt;
line-height:150%'><span lang=EN-ID style='font-size:12.0pt;line-height:150%;
font-family:"Times New Roman",serif'>Ruang lingkup aktivitas perkebunan
                                sebagaimana perkebunan adalah segala kegiatan pengelolaan sumber daya alam,
                                sumber daya manusia, sarana produksi, alat dan mesin, budi daya, panen,
                                pengolahan, dan pemasaran, terkait tanaman perkebunan. Pada kenyataannya sejauh
                                ini sudah menjadi bagian dari kehidupan keseharian masyarakat Kalimantan
                                Tengah. Dinas Perkebunan Provinsi Kalimantan Tengah adalah salah satu bagian
                                dari berbagai dinas yang ada di Provinsi Kalimantan Tengah. Dinas Perkebunan
                                Provinsi Kalimantan Tengah memiliki banyak bagian yang tugas nya secara
                                langsung berkaitan dengan pelayanan kepada masyarakat baik itu petani maupun
                                perusahaan yang bekerja sama dengan dinas perkebunan. </span></p>

                <p class=MsoNormal style='margin-top:0in;margin-right:0in;margin-bottom:0in;
margin-left:21.3pt;text-align:justify;text-indent:28.35pt;line-height:150%'><span lang=EN-ID
                                style='font-size:12.0pt;line-height:150%;font-family:"Times New Roman",serif'>Selama
                                pandemi covid-19 ini terjadi, pekerjaan kantor di Dinas Perkebunan Provinsi
                                Kalimantan Tengah yang semula dimulai jam 7 pagi menjadi jam 8 pagi. Bahkan ada
                                yang datang pada jam 10 pagi dan tidak sedikit juga yang mengerjakan perkerjaan
                                kantor dirumah, guna mengurangi kerumunan. Sehingga mengalami kendala untuk
                                beberapa pekerjaan. Salah satunya yaitu pada pelaporan kinerja. Pelaporan
                                kinerja masih berbentuk hard copy, sedangkan untuk meminta pelaporan kinerja
                                tersebut mengalami kendala, salah satunya yaitu ketika pegawai tidak hadir ke
                                kantor atau ketika pegawai sulit dihubungi. Permintaan pembuatan pelaporan
                                kinerja tidak terjadwal, sehingga pegawai yang membuat pelaporan kinerja tidak
                                mengerjakan laporan kinerja dengan tepat waktu. Oleh karena itu dibuatlah suatu
                                i TPKADsi untuk membuat aplikasi pelaporan kinerja berbasis website. Yang mana
                                dapat membantu pegawai untuk membuat pelaporan kinerja, mengupload laporan kinerja,
                                serta dapat mengingatkan pegawai yang bersangkutan untuk mengerjakan pelaporan
                                jika belum mengumpulkan laporan kinerja. </span></p>

